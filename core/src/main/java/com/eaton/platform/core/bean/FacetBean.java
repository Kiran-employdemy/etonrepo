package com.eaton.platform.core.bean;

public class FacetBean {
	
	private String FacetID;
	private String FacetDeselectURL;
	private String label;
	
	public String getFacetID() {
		return FacetID;
	}
	public void setFacetID(String facetID) {
		FacetID = facetID;
	}
	public String getFacetDeselectURL() {
		return FacetDeselectURL;
	}
	public void setFacetDeselectURL(String facetDeselectURL) {
		FacetDeselectURL = facetDeselectURL;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	
	

}
